function onMouseOverSO() {
    document.getElementById("so").className = "itemover";
    document.getElementById("flower").className = "navover";
    menu.style.display = "block";
}

function onMouseOutSO() {
    document.getElementById("so").className = "item";
    document.getElementById("flower").className = "nav";
    menu.style.display = "none";
}

